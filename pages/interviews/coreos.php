            <h2>Overview:</h2>
            <p>The WiC Project Team is creating a Magic Mirror. The magic mirror will display information such as the weather and Facebook events, without preventing the owner from seeing their reflection.  The mirror will be presented at the 11th annual Imagine RIT event on April 28, 2018. This project is being developed by Wic members in multidisciplinary majors within Golisano. This project has been in the works for 7 weeks and WiC plans to have its first prototype by the end of the fall semester. The finishing design touches will be polished throughout the Spring Semester. </p>
            <h2>What is a Magic Mirror:
            </h2>
            <p>A Magic Mirror is a mirror that displays helpful, or relevant information like the weather, time and date. It is composed of a raspberry pi, a monitor, and a two way mirror. The raspberry pi is used to display white text on a black background which will shine through the two way mirror and display text. The goal is to add to these basic features and more complex components such as motion sensors, facial recognition, and sound.</p>
            <h2>Our Progress:
            </h2>
            <p>Currently our team has brainstormed ideas of what features we would like to include in our Magic Mirror. We have come up with a few features like informing the user of information about WiC, displaying the current date and time, and informing the user of the weather as our minimum viable product. We have sketched a design of what we want it to look like. We have taken measurements of the monitor after breaking down the neck piece and removing the bezel. Those measurements are being used as a base to start measuring the wooden frame pieces in the Construct. We have created a scaled paper model to double check our measurements before we build the actual product.
            </p>
            <p>Currently we are cutting up the final frames in the Construct and have plans to cut the glass sheet to create the “mirror” aspect to our project. We are selecting the designs that we will to engrave on the face of the Magic Mirror. We plan to assemble the frame with nails and screws after we stain the wood.
            </p>
            <h2>Future Goals:</h2>
            <p>Our next step is to start working on the software component of the Magic Mirror. We want to utilize resources on campus including working with professors and an online Discord channel dedicated to providing help for future creators of Magic Mirrors!
            </p>

