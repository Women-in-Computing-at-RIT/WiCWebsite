                <h3>How can I make my resume stand out?</h3>
                    <ul>
                        <li>Assume companies do not know how much work goes into your clubs based solely on their names. Provide data on what you did for the club, how much time you spent working, what you improved (use percentages).
                        </li>
                        <li>Also, if you do not have a large amount of work experience, a leadership section will demonstrate your ability to work within a company. Freshman are encouraged to talk about the type of projects they worked on in class.</li>
                    </ul>

                <h3>How do I navigate the career fair?</h3>
                <ul>
                    <li>Research companies before going to the fair, and learn which positions the company is looking to fill.
                    </li>
                    <li>Try printing out the job descriptions the companies have posted and highlight/annotate the important things and how they apply to you. This will give you some perspective on what the company is looking for and what you have to offer them.
                    </li>
                    <li>If a recruiter is holding something, it is possible that he/she is avoiding a handshake to prevent sickness. Do not feel the need to get them to put down what they are holding to shake hands. </li>
                    <li>Hold on to any business cards you are given. Try to jot down a short note on the back about something you discussed with them to help you remember your conversation later when following up.</li>
                </ul>
                <h3>How can a student make a good impression with their interviewer?
                </h3>
                <ul>
                    <li>Come prepared with questions about the job and the company, and steer towards employers that show that they have opportunities for personal and professional growth.
                    </li>
                    <li>Try to avoid questions that can be found on Google. Good questions to ask might be: What is the social environment in the workplace? Do you often socialize with your co-workers outside the office? Why have you stayed with the company as long as you have?
                    </li>
                    <li>After the interview, keep in contact with the interviewer. Sending a nice thank you note can go a long way.</li>
                </ul>
                <h3>What skills have helped you to get the job and continue to help you in your workplace?</h3>
                    <ul>
                        <li>Technical experience that meets the role is what employers look for initially, but what gets you the job is your positive attitude and willingness to learn. The most important thing is that you are excited to learn and are a delight to be with in the conference room.</li>
                    </ul>

